def task3():
    """
    Дан список размера N. Осуществить сдвиг элементов списка влево на одну позицию
    (при этом AN перейдет в AN-1, AN-1 — в AN-2, .., A2 — в A1, a исходное значение
    первого элемента будет потеряно). Последний элемент полученного списка
    положить равным 0.
    """
    # Пример списка (можно заменить на ввод пользователя)
    A = [1, 2, 3, 4, 5]
    print(f"Исходный список: {A}")
    
    # Выполняем сдвиг влево
    # Используем срез для получения элементов со 2-го до конца
    shifted_A = A[1:]
    
    # Добавляем 0 в конец списка
    shifted_A.append(0)
    
    # Выводим результат
    print(f"Список после сдвига: {shifted_A}")

# Вызов функции для задачи 3
task3()
