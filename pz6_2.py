def task2():
    """
    Дано число R и список A размера N. Найти элемент списка, который наиболее близок
    к числу R (то есть такой элемент AK, для которого величина |AK - R| является
    минимальной).
    """
    # Пример данных (можно заменить на ввод пользователя)
    R = 15
    A = [10, 20, 5, 30, 12, 18, 14]
    
    # Инициализируем переменные для хранения минимальной разницы и ближайшего элемента
    min_diff = float('inf')  # Начинаем с бесконечности
    closest_element = None
    
    # Перебираем все элементы списка
    for num in A:
        current_diff = abs(num - R)
        # Если текущая разница меньше минимальной, обновляем значения
        if current_diff < min_diff:
            min_diff = current_diff
            closest_element = num
    
    # Выводим результат
    print(f"Число R: {R}")
    print(f"Список A: {A}")
    print(f"Наиболее близкий элемент к R: {closest_element} (разница = {min_diff})")

# Вызов функции для задачи 2
task2()
